    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int prioridad=Integer.parseInt(jTextField2.getText());
        boolean r=LDPO.Insertar(jTextField1.getText().charAt(0), prioridad);
        if(r==true){
            JOptionPane.showMessageDialog(null, "SE INGRESO EL VALOR");
            MostrarLista();
            jTextField1.setText("");
        }else{
            JOptionPane.showMessageDialog(null, "COLA LLENA");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

